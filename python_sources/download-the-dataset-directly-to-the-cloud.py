# If you are still struggling with downloading the data. Here is a way to download it using Kaggle API (don't get frustrated, this very simple to setup:) ). 
# 
# This will be very useful for those who are working on the cloud and may fine uploading the data is very annoying and time consuming. So here we go: 
# 
# 
# I will assume you have Kaggle API already set up on your machine. If it's not the case, here is a quick [guide](https://github.com/Kaggle/kaggle-api) (will take less than 10 min to get it done :) ). 
# 
# Going back to the dataset of this challenge, type this on your terminal : 
# 
#         kaggle datasets download abhishek/siim-dicom-images
# 
# This will download the data from the dataset uploaded by [Abhishek](https://www.kaggle.com/abhishek/siim-dicom-images/downloads/siim-dicom-images.zip/1) and you will see the data being downloaded directy to your machine. 
# 
# You can also download it from [See-](https://www.kaggle.com/seesee). Just replace the directory of the dataset as follow: 
# 
#          kaggle datasets download seesee/siim-train-test
