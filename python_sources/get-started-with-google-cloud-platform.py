# In this notebook, you'll learn how to set up your **Google Cloud Platform (GCP)** account and get $300 of free credits!
# 
# # Step 1: Create a Google account
# 
# Begin by navigating to **[this page](https://console.cloud.google.com)**.  It will bring you to this window:
# 
# ![](https://i.imgur.com/DmFkln4.png)
# 
# If you already have a Google account (or e-mail address through Gmail), you can use it to sign in.  Otherwise, click on **Create account** to create a new Google account.

# # Step 2: Create a Google Cloud Platform account
# 
# Once you have created a Google account, the next step is to create a Google Cloud Platform account.  
# 
# To do this, select your country.  Then review and agree to the Terms of Service.  Click on **AGREE AND CONTINUE** to proceed.
# 
# ![](https://i.imgur.com/3z2EaNE.png)

# # Step 3: Activate your free trial
# 
# In this section, you'll learn how to get $300 of free credits that you can use for Google Cloud products (like Google Cloud AutoML).  
# 
# ## Claim your free credits
# 
# Begin by clicking on **TRY FOR FREE**.  
# 
# ![](https://i.imgur.com/ca8zQSk.png)
# 
# This will navigate you to a new window.
# 
# ![](https://i.imgur.com/UyXkZPK.png)
# 
# Select your country.  Then review and agree to the Terms of Service.  Hit **CONTINUE**.
# 
# This brings you to a second form where you have to select your account type, fill in your name and address, and provide a payment method.  
# 
# ![](https://i.imgur.com/gzhzInN.png)
# 
# Once you have filled in these items, click on **START MY FREE TRIAL**.
# 
# ## Verify your account settings
# 
# After the free period ends, you will not be charged unless you manually upgrade to a paid account.  
# 
# To check that you do not have a paid account, begin by navigating to the **Overview** page of the **Billing** tab.
# 
# ![](https://i.imgur.com/ubexCXA.png)
# 
# Scroll down, and look for a **Free trial credit** window.  
# 
# ![](https://i.imgur.com/b6iiKaR.png)
# 
# If you see "_You will not be billed during your free trial. To keep your projects running after the free trial is up, upgrade to a paid account._", then you will not be charged in the event that all of your credits are used.

# # Step 4: Create a project
# 
# Navigate to **[this page](https://console.cloud.google.com/cloud-resource-manager?_ga=2.226804948.812367449.1587743569-2053098250.1587743396)**, and click on **CREATE PROJECT** to create a new project.
# 
# ![](https://i.imgur.com/KG5nyNu.png)
# 
# Fill in a name for your project.  This will generate a project ID (in this case, my project ID is `'automl-exercise'`).  Then hit **CREATE**.
# 
# ![](https://i.imgur.com/kczvhus.png)
# 
# Then, navigate to [this page](https://console.cloud.google.com/flows/enableapi?apiid=storage-component.googleapis.com,automl.googleapis.com,storage-api.googleapis.com&redirect=https://console.cloud.google.com&_ga=2.32030718.-119742050.1571893794) to enable Cloud AutoML and Storage APIs.
# 
# Once you have followed these steps, you're ready to get started with Google Cloud on Kaggle!
