# * Since all the companies are concerned with tech, there is the expected increase in acquisitions starting 1994, during the era of the Dot Com Bubble and perhaps as a part of it.
# * Google and IBM made their first acquisitions in 2001.
# * 2005 saw the revival of mergers, with a marked increase in the number and scale of companies. All seven companies were active by this time.
# * The recession period of 2008-2009 saw acquisitions fall to their lowest point (value-wise) for the previous 4 and the next 7 years.
# * 2010 again saw the revival of the merger cycle, with a number of small companies being acquired.
# * 2011 saw the acquisition of Motorola Mobility by Google and Skype Technologies by Microsoft, pushing the value of acquisitions to a historic high.
# * WhatsApp was acquired by Facebook in 2014 for USD 19 billion. In the same year, Apple acquired Beats Electronics and Google Nest Labs. Google remained the most prolific in the acquisition market.
# * The number of acquisitions has seen a steady faal since 2014. 2016 was highlighted by the Acquisition of LinkedIn by Microsoft. The value reached a 11-year low in 2017. 
