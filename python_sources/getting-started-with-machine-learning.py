# **Machine Learning**
# 
# Let's have a look at the defnition of Machine Learning by [Tom M. Mitchel](https://en.wikipedia.org/wiki/Tom_M._Mitchell)
# 
# >    A computer program is said to learn from experience E with respect to some class of tasks T and performance measure P if its performance at tasks in T, as measured by P, improves with experience E.
#     
#  Layman terms
#        
# >  Machine learning provides computers with the ability to learn without being explicitly programmed.
# 
# **Keywords and Terms**
#        
#    Uderfitting
#     
# >    Model or Algorithm doesnot fit the data well enough. It usually happens when we have less data to build an accurate model and also when we try to build a linear model and also when we try to build a linear model with a non-linear data. 
#  
#  Overfitting
#  
# >   Model or algorithm is said to be overfitted, when we train it with a lot of data (just like fitting ourselves in an oversized pants!). When a model gets trained with so much of data. It starts learning from noise & inaccurate data entries in our data sets. Then the model doesnot categorize the data correctly because of too much of details of noise.
# 
# Cross Validation
#   
# >   A statistical technique to estimate the prediction error rate by splitting the data into training, cross validation and test datasets.
# 
# Supervised Learning 
#  
# >  supervised learning is a learning in which we teach or train the machine using data which is well labeled that means some data is already tagged with correct answer.
# 
# Unsupervised Learning 
# 
# > Unsupervised learning is a branch of machine learning that learns from test data that has not been labeled, classified or categorized. 
# 
# Classification 
# 
# > In machine learning and statistics, classification is the problem of identifying to which of a set of categories (sub-populations) a new observation belongs, on the basis of a training set of data containing observations (or instances) whose category membership is known.
# 
# Clustering
# 
# > Clustering is the task of dividing the population or data points into a number of groups such that data points in the same groups are more similar to other data points in the same group and dissimilar to the data points in other groups. It is basically a collection of objects on the basis of similarity and dissimilarity between them.
# 
# Training dataset
# 
# > A training dataset is a dataset of examples used for learning, that is to fit the parameters.
# 
# Validation dataset
# 
# > The sample of data used to provide an unbiased evaluation of a model fit on the training dataset while tuning model hyperparameters. The evaluation becomes more biased as skill on the validation dataset is incorporated into the model configuration.
# 
# Test dataset
# 
# > The sample of data used to provide an unbiased evaluation of a final model fit on the training dataset.
